V_ADM_GRU SNG001.SNG001ASE%TYPE;
V_ADM_GRU_S SNG001.SNG001ASE%TYPE;
V_CORRE_GRU SNG001.SNG001ASE%TYPE;
V_CORRE_GRU_S SNG001.SNG001ASE%TYPE;
V_TERRI_GRU SNG001.SNG001ASE%TYPE;
V_TERRI_GRU_S SNG001.SNG001ASE%TYPE;

BEFORE EACH ROW IS
BEGIN
    V_USR_ING := TRIM(:NEW.WFWRKLSTUSRCOD);

    SELECT WFINSPRCID,WFTASKCOD,WFITEMID,WFITEMPRVTASK,WFPRCID INTO V_INSTANCIA,V_ETAPA,V_ITEM,V_PRVTSK,V_WFPRCID FROM WFWRKITEMS WHERE WFITEMID = :NEW.WFWRKLSTITEMID;
    select MAX(SNG091AUT) INTO V_NROAUT from sng091 WHERE sng001inst = V_INSTANCIA;

    IF V_ETAPA = 11 AND V_WFPRCID = 1 THEN --CAMBIO RASM VALTX7 SE ESPECIFICA QUE SEA SOLO PARA PROCESO 1
        SELECT COUNT(1) INTO V_FLAG_ASE FROM JNGZ782 WHERE Z782INS = V_INSTANCIA AND Z782AUT = V_NROAUT;
        SELECT Z782USR,Z782CAR INTO V_ASE,V_CARGO FROM JNGZ782 WHERE Z782INS = V_INSTANCIA AND Z782AUT = V_NROAUT;
    END IF;

    /* Validacion para aprobacion PDM  INICIO*/
    IF V_ETAPA = 11 AND V_WFPRCID = 3   THEN

          V_ADM_UPD := 1;
          SELECT SNG001ASE INTO V_SNG001ASE FROM SNG001 WHERE SNG001INST = V_INSTANCIA;

          SELECT COUNT(*) INTO V_CAR212 FROM sng065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 212 AND SNG065EST = 'P' AND SNG065RES = 'P';
          SELECT COUNT(*) INTO V_CAR320 FROM sng065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 320 AND SNG065EST = 'P' AND SNG065RES = 'P';

          --OBTIENE SUPERVISOR RESPONSABLE
          SELECT T470USUG INTO V_ADMIN FROM JFCT470 WHERE T470EMP = 1 AND T470USUD = V_SNG001ASE AND T470NIV = 10;

          Select (T470USUG) into V_RIESGOS from JFCT470
          where T470USUD = (Select (T470USUG) from JFCT470 where T470USUD = V_ADMIN AND T470NIV = 20) AND  T470NIV = 50;

          SELECT PGFAPE INTO V_FECSIS   FROM FST017 WHERE PGCOD = 1;

          --INICIA BUSQUEDA SUPLENCIA ACTIVA
          BEGIN
            SELECT SNG057SUP INTO V_SUP   FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADMIN   AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
            EXCEPTION WHEN OTHERS THEN
            V_SUP := ' ';
         END;

         BEGIN
            SELECT SNG057SUP INTO V_SUP_R FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_RIESGOS AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
            EXCEPTION WHEN OTHERS THEN
            V_SUP_R := ' ';
         END;

         IF LENGTH(TRIM(V_SUP)) > 1 THEN
            V_ADMIN := V_SUP;
         END IF;

         IF LENGTH(TRIM(V_SUP_R)) > 1 THEN
            V_RIESGOS := V_SUP_R;
         END IF;
         --FIN BUSQUEDA SUPLENCIA ACTIVA
        --V_ADM_UPD := 1;

    END IF;
    /* Validacion para aprobacion PDM FIN*/

    /* Validacion para aprobacion CREDITO PARALELO INICIO*/
    IF V_ETAPA = 11 AND V_WFPRCID = 5   THEN

          V_ADM_UPD := 1;
          SELECT SNG001ASE INTO V_SNG001ASE FROM SNG001 WHERE SNG001INST = V_INSTANCIA;

          SELECT COUNT(*) INTO V_CAR212 FROM sng065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 212 AND SNG065EST = 'P' AND SNG065RES = 'P';
          SELECT COUNT(*) INTO V_CAR320 FROM sng065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 320 AND SNG065EST = 'P' AND SNG065RES = 'P';

          --OBTIENE SUPERVISOR RESPONSABLE
          SELECT T470USUG INTO V_ADMIN FROM JFCT470 WHERE T470EMP = 1 AND T470USUD = V_SNG001ASE AND T470NIV = 10;

          SELECT PGFAPE INTO V_FECSIS   FROM FST017 WHERE PGCOD = 1;

          --INICIA BUSQUEDA SUPLENCIA ACTIVA
          BEGIN
            SELECT SNG057SUP INTO V_SUP   FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADMIN   AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
            EXCEPTION WHEN OTHERS THEN
            V_SUP := ' ';
         END;

         IF LENGTH(TRIM(V_SUP)) > 1 THEN
            V_ADMIN := V_SUP;
         END IF;
         --FIN BUSQUEDA SUPLENCIA ACTIVA

    END IF;
    /* Validacion para aprobacion CREDITO PARALELO FIN*/

exception
when OTHERS then
	V_FLAG_ASE := 0;
END BEFORE EACH ROW;

AFTER EACH ROW IS
BEGIN
    V_USUARIO := ' ';
    SELECT  nvl(PF.UBUSER,' ') INTO V_USUARIO FROM PRFU00 PF
    INNER JOIN prfg00 PG ON PG.PGCOD=PF.PGCOD AND PG.PRFGCOD=PF.PRFGCOD
    WHERE PF.PGCOD=1 AND PF.UBUSER=:new.WFWRKLSTUSRCOD AND TRIM(PG.PRFGCOD) IN (SELECT  TRIM(TPDESC) FROM FST098 WHERE PGCOD=1 AND TPCOD=15318 AND TPCORR<>0 AND TPNRO=1) and rownum=1 order by UBUSER;

    IF V_USUARIO <> ' ' THEN -- CAMBIO DE TREGG001, INGRESA SOLO CUANDO TENGAN ALGUNOS DE LOS PERFILES REGISTRADOS EN LA GUIA 15318
        IF V_ETAPA = 11 THEN --CAMBIO DE TREGG001 SVT 3320 11/10/19  INFORMAR A NIVEL APROBADOR
            SELECT  TPNRO INTO V_GUIA FROM FST098 WHERE PGCOD=1 AND TPCOD=15313 AND TPCORR=1;
            IF V_GUIA=1 THEN
                SELECT MAX(FRALEID) + 1 INTO V_FRALEID FROM FRALERTS;
                INSERT INTO FRALERTS (FRALEID, FRALETEXT, FRALECALL, FRALEUSER, FRALETYPE, FRALEDATE)
                VALUES(V_FRALEID, 'Nueva Instancia Pendiente: ' || to_char(V_INSTANCIA), '', :new.WFWRKLSTUSRCOD,'I',SYSDATE);
            END IF;

            SELECT  TPNRO INTO V_GUIA FROM FST098 WHERE PGCOD=1 AND TPCOD=15313 AND TPCORR=2;
            IF V_GUIA=1 THEN
                SELECT  z449mail INTO V_MAIL FROM JNGZ449 WHERE Z449USU =:new.WFWRKLSTUSRCOD;
                IF V_MAIL <> '' OR V_MAIL <> ' ' THEN
                    PA_ENVIO_CORREO (p_DestinatariosPara => V_MAIL,p_DestinatariosCC => '',p_Remitente => 'operaciones.bantotal@confianza.pe',p_Mensaje => 'Tiene una instancia pendiente N: ' || to_char(V_INSTANCIA) || '. No responda este mensaje, es un env?-o autom?!tico.', p_TipoMensaje => 'HTML',p_Asunto => 'OPERACI??N DE INSTANCIA PENDIENTE N: ' || to_char(V_INSTANCIA));
                END IF;
            END IF;
        END IF;
    END IF;
exception
when OTHERS then
	V_MAIL := ' ';
END AFTER EACH ROW;

AFTER STATEMENT IS
BEGIN
    IF V_ETAPA = 11 THEN --VERIFICA QUE LA INSTANCIA ESTA EN ETAPA DE APROBACION

        --INICIA AUTONOMIA ASESOR
        IF V_FLAG_ASE = 1 THEN
            UPDATE SNG065 SET SNG065CAR = V_CARGO WHERE SNG062AUT = V_NROAUT;
            UPDATE WFWORKLIST SET WFWRKLSTUSRCOD = V_ASE WHERE WFWRKLSTITEMID = V_ITEM;
            DELETE SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 100 AND SNG065ORD=2;
        END IF;
        --FIN AUTONOMIA ASESOR

        --INICIA NUEVAS AUTONOMIAS - Direccionamiento de administrador
        V_OK := 0;
        BEGIN
            --VERIFICA SI LA TAREA ESTA PENDIENTE DE EVALUACION POR UN ADMINISTRADOR
            V_ADM := 0;
            SELECT COUNT(*) INTO V_ADM FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 212 AND TRIM(SNG065USR) IS NULL;

            --CARGO DEL NUEVO REGISTRO
            BEGIN
                SELECT SNG055CAR INTO V_SNG055CAR
                FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_USR_ING;
            EXCEPTION WHEN OTHERS THEN
                V_SNG055CAR := 0;
            END;

            IF V_ADM = 1 AND V_SNG055CAR = 212 THEN
                --OBTIENE ASESOR DE CREDITO
                SELECT SNG001ASE INTO V_SNG001ASE FROM SNG001 WHERE SNG001INST = V_INSTANCIA;

                --OBTIENE SUPERVISOR RESPONSABLE - Reutilizando variable V_ASE
                SELECT T470USUG INTO V_ASE FROM JFCT470 WHERE T470EMP = 1 AND T470USUD = V_SNG001ASE AND T470NIV = 10;

                --INICIA BUSQUEDA SUPLENCIA ACTIVA
                BEGIN
                    SELECT PGFAPE INTO V_FECSIS FROM FST017 WHERE PGCOD = 1;
                    SELECT SNG057SUP INTO V_SUP FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ASE AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
                EXCEPTION WHEN OTHERS THEN
                    V_SUP := ' ';
                END;

                IF LENGTH(TRIM(V_SUP)) > 1 THEN
                    V_ASE := V_SUP;
                END IF;
                --FIN BUSQUEDA SUPLENCIA ACTIVA

                V_OK := 1;
            END IF;

        EXCEPTION WHEN OTHERS THEN
            V_OK := 0;
        END;

        IF V_OK = 1 THEN
            --ELIMINA LISTA DE CANDIDATOS
                UPDATE WFWORKLIST SET WFWRKLSTUSRCOD = V_ASE WHERE WFWRKLSTITEMID = V_ITEM AND WFWRKLSTUSRCOD = V_USR_ING;
                DELETE FROM WFWORKLIST WHERE WFWRKLSTITEMID = V_ITEM AND WFWRKLSTUSRCOD <> V_ASE;
        END IF;
        --FIN NUEVAS AUTONOMIAS - Direccionamiento de administrador

    END IF;
    --V_LOG := V_LOG || '1';
    /* Validacion para aprobacion PDM INICIO*/
    --CARGO DEL NUEVO REGISTRO
    V_CONTAR_C := 0;
    BEGIN
        SELECT COUNT(SNG055CAR) INTO V_CONTAR_C
        FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_USR_ING AND SNG055CAR IN (241,242,243);
    EXCEPTION WHEN OTHERS THEN
        V_SNG055CAR := 0;
    END;
    V_CONTAR_A := 0;
    BEGIN
        SELECT COUNT(SNG055CAR) INTO V_CONTAR_A
        FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_USR_ING AND SNG055CAR IN (242,243);
    EXCEPTION WHEN OTHERS THEN
        V_SNG055CAR := 0;
    END;
    IF V_ETAPA = 11 AND V_WFPRCID = 3 AND V_ADM_UPD = 1 THEN

      SELECT PGFAPE INTO V_FECSIS FROM FST017 WHERE PGCOD = 1;

      select COUNT(SNG001ASE) INTO VCONTROL FROM SNG001 WHERE sng001inst = V_INSTANCIA;
        IF VCONTROL > 0 THEN

          select SNG001ASE INTO V_ASE_GRU FROM SNG001 WHERE sng001inst = V_INSTANCIA;
          VCONTROL := 0;
          SELECT COUNT(T470USUG) INTO VCONTROL FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ASE_GRU AND T470NIV=5 AND T470AGEV=99999;
            --TIENE COORDINADOR
            IF VCONTROL > 0 THEN
              SELECT T470USUG INTO V_COOR_GRU FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ASE_GRU AND T470NIV=5 AND T470AGEV=99999;

              --COORDINADOR PRIMER NIVEL
              SELECT COUNT(SNG057SUP) INTO VCONTROL FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_COOR_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
              V_BAND := 0;
              V_ACTUALIZA := 0;
              IF VCONTROL > 0 THEN
                SELECT SNG057SUP INTO V_COOR_GRU_S FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_COOR_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA  FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 216 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1 AND V_CONTAR_C = 0 THEN
                  UPDATE SNG065 SET SNG065USR = V_COOR_GRU_S WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 216 AND SNG065EST = 'P' AND SNG065RES = 'P'; --Coordinador grupal
                  --VISUALIZAR EN BANDEJA
                  UPDATE  WFWORKLIST SET WFWRKLSTUSRCOD = V_COOR_GRU_S WHERE WFWRKLSTITEMID = V_ITEM;
                  UPDATE WFWRKITEMS SET WFITEMUSRCOD = V_COOR_GRU_S WHERE WFINSPRCID = V_INSTANCIA AND WFITEMID = V_ITEM;
                END IF;
              ELSE
                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA  FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 216 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1  AND V_CONTAR_C = 0 THEN
                  UPDATE SNG065 SET SNG065USR = V_COOR_GRU WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 216 AND SNG065EST = 'P' AND SNG065RES = 'P'; --Coordinador grupal
                  --VISUALIZAR EN BANDEJA
                  UPDATE  WFWORKLIST SET WFWRKLSTUSRCOD=V_COOR_GRU WHERE WFWRKLSTITEMID = V_ITEM;
                  UPDATE WFWRKITEMS SET WFITEMUSRCOD = V_COOR_GRU WHERE WFINSPRCID = V_INSTANCIA AND WFITEMID = V_ITEM;
                END IF;
              END IF;

              --ADMINISTRADOR
              V_ACTUALIZA := 0;
              SELECT COUNT(T470USUG) INTO VCONTROL FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_COOR_GRU AND T470NIV=7 AND T470AGEV=99999;
              IF VCONTROL > 0 THEN
                SELECT T470USUG INTO V_ADM_GRU FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_COOR_GRU AND T470NIV=7 AND T470AGEV=99999;
                SELECT COUNT(SNG057SUP) INTO VCONTROL FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADM_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;

                IF VCONTROL > 0 THEN

                  SELECT SNG057SUP INTO V_ADM_GRU_S FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADM_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;

                  SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE  SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';
                  IF V_ACTUALIZA = 1 THEN
                    UPDATE SNG065 SET SNG065USR = V_ADM_GRU_S WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241;  --Administrador grupal
                  END IF;
                ELSE
                  SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE  SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';
                  IF V_ACTUALIZA = 1 THEN
                    UPDATE SNG065 SET SNG065USR = V_ADM_GRU WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241;  --Administrador grupal
                  END IF;
                END IF;
              END IF;
            ELSE
              --ADMINISTRADOR PRIMER NIVEL
              SELECT COUNT(T470USUG) INTO VCONTROL FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ASE_GRU AND T470NIV=10 AND T470AGEV=99999;
              V_ACTUALIZA := 0;
              IF VCONTROL > 0 THEN
                SELECT T470USUG INTO V_ADM_GRU FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ASE_GRU AND T470NIV=10 AND T470AGEV=99999;
                SELECT COUNT(SNG057SUP) INTO VCONTROL FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADM_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
                IF VCONTROL > 0 THEN
                  SELECT SNG057SUP INTO V_ADM_GRU_S FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_ADM_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
                  SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';
                  IF V_ACTUALIZA = 1 AND V_CONTAR_A = 0 THEN
                    UPDATE SNG065 SET SNG065USR = V_ADM_GRU_S WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';  --Administrador grupal
                    --VISUALIZAR EN BANDEJA
                    UPDATE  WFWORKLIST SET WFWRKLSTUSRCOD=V_ADM_GRU_S WHERE WFWRKLSTITEMID = V_ITEM;
                    UPDATE WFWRKITEMS SET WFITEMUSRCOD = V_ADM_GRU_S WHERE WFINSPRCID = V_INSTANCIA AND WFITEMID = V_ITEM;
                  END IF;
                ELSE
                  SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';
                  IF V_ACTUALIZA = 1 AND V_CONTAR_A = 0 THEN
                    UPDATE SNG065 SET SNG065USR = V_ADM_GRU WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 241 AND SNG065EST = 'P' AND SNG065RES = 'P';  --Administrador grupal
                    --VISUALIZAR EN BANDEJA
                    UPDATE  WFWORKLIST SET WFWRKLSTUSRCOD=V_ADM_GRU WHERE WFWRKLSTITEMID = V_ITEM;
                    UPDATE WFWRKITEMS SET WFITEMUSRCOD = V_ADM_GRU WHERE WFINSPRCID = V_INSTANCIA AND WFITEMID = V_ITEM;
                  END IF;
                END IF;
              END IF;

            END IF;

            --CORREDOR
            V_ACTUALIZA := 0;
            SELECT COUNT(T470USUG) INTO VCONTROL FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ADM_GRU AND T470NIV=20 AND T470AGEV=99999;
            IF VCONTROL > 0 THEN
              SELECT T470USUG INTO V_CORRE_GRU FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_ADM_GRU AND T470NIV=20 AND T470AGEV=99999;
              SELECT COUNT(SNG057SUP) INTO VCONTROL FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_CORRE_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
              IF VCONTROL > 0 THEN
                SELECT SNG057SUP INTO V_CORRE_GRU_S FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_CORRE_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;

                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 242 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1 THEN
                  UPDATE SNG065 SET SNG065USR = V_CORRE_GRU_S WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 242; --Corredor grupal
                END IF;
              ELSE
                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 242 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1 THEN
                  UPDATE SNG065 SET SNG065USR = V_CORRE_GRU WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 242; --Corredor grupal
                END IF;
              END IF;
            END IF;

            --TERRITORIAL
            V_ACTUALIZA := 0;
            SELECT COUNT(T470USUG) INTO VCONTROL FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_CORRE_GRU AND T470NIV=30 AND T470AGEV=99999;
            IF VCONTROL > 0 THEN
              SELECT T470USUG INTO V_TERRI_GRU FROM JFCT470 WHERE T470EMP=1 AND T470USUD=V_CORRE_GRU AND T470NIV=30 AND T470AGEV=99999;
              SELECT COUNT(SNG057SUP) INTO VCONTROL FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_TERRI_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
              IF VCONTROL > 0 THEN
                SELECT SNG057SUP INTO V_TERRI_GRU_S FROM SNG057 WHERE SNG055EMP = 1 AND SNG057USR = V_TERRI_GRU AND SNG057INI <= V_FECSIS AND SNG057FIN >= V_FECSIS;
                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 243 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1 THEN
                  UPDATE SNG065 SET SNG065USR = V_TERRI_GRU_S WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 243; --Territorial grupal
                END IF;
              ELSE
                SELECT COUNT(SNG065USR) INTO V_ACTUALIZA FROM SNG065 WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 243 AND SNG065EST = 'P' AND SNG065RES = 'P';
                IF V_ACTUALIZA = 1 THEN
                  UPDATE SNG065 SET SNG065USR = V_TERRI_GRU WHERE SNG062AUT = V_NROAUT AND SNG065CAR = 243; --Territorial grupal
                END IF;
              END IF;
            END IF;
        END IF;
    END IF;
  /* Validacion para aprobacion PDM FIN */

  --NUEVO RASM VALTX7 AUTONOMÍAS PARA CRÉDITO PARALELO
   /* Validacion para aprobacion Crédito Paralelo INICIO*/
    --CARGO DEL NUEVO REGISTRO
    V_CONTAR_C := 0;
