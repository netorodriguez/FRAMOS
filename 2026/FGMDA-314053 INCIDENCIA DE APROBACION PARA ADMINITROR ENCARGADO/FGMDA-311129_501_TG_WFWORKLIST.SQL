V_ADM NUMBER(5);
V_FECSIS DATE;
V_SUP SNG057.SNG057SUP%TYPE;

V_OK NUMBER(2);

V_USR_ING CHAR(10);
V_SNG055CAR SNG057.SNG055CAR%TYPE;

/*PDM*/
V_WFPRCID WFWRKITEMS.WFPRCID%TYPE;
V_ADMIN   SNG001.SNG001ASE%TYPE;
V_ADM_UPD NUMBER(2);
V_CAR212  NUMBER(2);
V_CAR320  NUMBER(2);
V_CAR320R NUMBER(2);
