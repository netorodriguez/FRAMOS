V_ETAPA WFWRKITEMS.WFTASKCOD%TYPE;
V_NROAUT sng091.sng091aut%TYPE;
V_FLAG_ASE NUMBER(1);
V_ITEM WFWORKLIST.WFWRKLSTITEMID%TYPE;
V_ASE JNGZ782.Z782USR%TYPE;
V_CARGO SNG057.SNG055CAR%TYPE;

V_PRVTSK WFWRKITEMS.WFITEMPRVTASK%TYPE;
V_MAIL JNGZ449.Z449MAIL%TYPE;
V_FRALEID NUMBER(6);
